# XXX suckish way of handling public variables
my %public_data :ATTR;
my %public_keys = map { $_ => 1 } qw(TOWER_OF_DESPAIR XAJORKITH USHGARAK LAKE_MIRROW);

sub AUTOMETHOD {
    my ($self, $ident, $args) = @_;
    my $key = $_;

    return if not $public_keys{$key};

    return sub {
        return $public_data{ident $self}->{$key} || ();
    };
}
    

# assigned at end of file
my (@main_map, @ref_desc_map, @routes);

my %game                :ATTR( :init_arg<game> );
my %locations           :ATTR;
my %route_nodes         :ATTR;

sub width {
    my ($self) = @_;

    return scalar @{$locations{ident $self}};
}

sub height {
    my ($self) = @_;

    return scalar @{$locations{ident $self}->[0]};
}

sub get_location {
    my ($self, $x, $y) = @_;

    if ($x < 0 or $y < 0 or
        $x >= @{$locations{ident $self}} or $y >= @{$locations{ident $self}->[0]}) {
        return Goodnight::Location::FrozenWaste->get_instance;
    }

    return $locations{ident $self}->[$x]->[$y];
}

sub set_location {
    my ($self, $location) = @_;

    $locations{ident $self}->[$location->get_x]->[$location->get_y] = $location;
}

sub get_in_front {
    my ($self, $location, $direction) = @_;

    my $x = $location->get_x + $direction->get_x;
    my $y = $location->get_y + $direction->get_y;

    return $self->get_location($x, $y);
}

sub get_looking_towards {
    my ($self, $location, $direction) = @_;

    for (0..2) {
        $location = $self->get_in_front($location, $direction);
        if ($location->get_feature != Goodnight::Location::Feature::PLAINS or $location->is_special) {
            last;
        }
    }

    return $location;
}

# XXX static method. this should be in Location.pm
sub calc_distance {
    my ($a, $b) = @_;

    return abs($a->get_x - $b->get_x) + abs($a->get_y - $b->get_y);
}

# XXX static method. this should be in Location.pm
sub calc_direction {
    my ($origin, $target) = @_;

    if ($origin->get_x > $target->get_x) {
        if ($origin->get_y > $target->get_y) {
            return Goodnight::Map::Direction::NORTHWEST;
        }
        elsif ($origin->get_y < $target->get_y) {
            return Goodnight::Map::Direction::SOUTHWEST;
        }
        else {
            return Goodnight::Map::Direction::WEST;
        }
    }

    elsif ($origin->get_x < $target->get_x) {
        if ($origin->get_y > $target->get_y) {
            return Goodnight::Map::Direction::NORTHEAST;
        }
        elsif ($origin->get_y < $target->get_y) {
            return Goodnight::Map::Direction::SOUTHEAST;
        }
        else {
            return Goodnight::Map::Direction::EAST;
        }
    }

    else {
        if ($origin->get_y > $target->get_y) {
            return Goodnight::Map::Direction::NORTH;
        }
        elsif ($origin->get_y < $target->get_y) {
            return Goodnight::Map::Direction::SOUTH
        }
        else {
            return undef;
        }
    }
}

sub START {
    my ($self, $ident, $args) = @_;

    my $locations = $locations{ident $self} = [];
    push @{$locations}, [] for 0..63;

    my $i = 0;
    for my $y (0..60) {
        for my $x (0..63) {
            $locations->[$x]->[$y] = Goodnight::Location->new({
                game    => $game{ident $self},
                x       => $x,
                y       => $y,
                feature => Goodnight::Location::Feature->by_ordinal($main_map[$i] & 0x0f),
                object  => Goodnight::Location::Object->by_ordinal($main_map[$i] >> 4),
                area    => Goodnight::Map::Area->by_ordinal($ref_desc_map[$i] & 0x3f),
                domain  => ($ref_desc_map[$i] & 0x40) != 0,
                special => ($ref_desc_map[$i] & 0x80) != 0,
            });

            $i++;
        }
    }

    $public_data{ident $self} = {
        TOWER_OF_DESPAIR => $self->get_location(26, 4),
        XAJORKITH        => $self->get_location(45, 59),
        USHGARAK         => $self->get_location(29, 7),
        LAKE_MIRROW      => $self->get_location(9, 17),
    };

    my $route_nodes = $route_nodes{ident $self} = {};
    for my $i (0 .. @routes-1) {
        $route_nodes->{ident $locations->[$routes[$i]->[0]]->[$routes[$i]->[1]]} = $i;
    }
}

sub get_route_node {
    my ($self, $index) = @_;

    return $locations{ident $self}->[$routes[$index]->[0]]->[$routes[$index]->[1]];
}

sub get_node_index {
    my ($self, $node) = @_;

    return $route_nodes{ident $self}->{ident $node};
}

sub get_next_node_a {
    my ($self, $location) = @_;

    my $next_node = $routes[$self->get_node_index($location)]->[2];
    return $self->get_route_node($next_node);
}

sub get_next_node_b {
    my ($self, $location) = @_;

    my $next_node = $routes[$self->get_node_index($location)]->[3];
    return $self->get_route_node($next_node);
}

